const SENTINEL_B = '|';
const SENTINEL_C = '^';

// Build encoding map: word -> { sentinel, code }
export const wordToTier2 = new Map<string, { sentinel: string; code: string }>();

function buildEncodingMap(words: string[], sentinel: string) {
	for (let i = 0; i < words.length; i++) {
		const word = words[i];
		if (!wordToTier2.has(word)) {
			wordToTier2.set(word, { sentinel, code: indexToCode(i) });
		}
	}
}

